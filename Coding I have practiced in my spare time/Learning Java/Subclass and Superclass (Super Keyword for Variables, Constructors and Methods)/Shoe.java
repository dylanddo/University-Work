// Java Inheritance (Subclass and Superclass) 
// Th subclass (child) inherit attributes and methods from another class superclass (parent).
public class Shoe
{
    public final String brand;
    public final double size;
    
    public Shoe(String brand, double size)
    {
        this.brand = brand;
        this.size = size;
    }
}
