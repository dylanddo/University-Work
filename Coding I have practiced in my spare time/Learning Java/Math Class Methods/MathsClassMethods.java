public class MathsClassMethods
{
    public static void main(String[] args)
    {
        System.out.println(Math.abs(-26.7)); // Takes any number negative or not and returns the absolute value.
        
        System.out.println(Math.ceil(7.4)); // Always rounds up
        System.out.println(Math.floor(7.8)); // Always rounds down 
        
        System.out.println(Math.max(8.6, 5.2)); // Gives biggest number
        System.out.println(Math.max(8.6, 5.2)); // Gives smallest number
        
        System.out.println(Math.pow(5, 3)); // Returns power
        System.out.println(Math.sqrt(9)); // Square root numbers
    }
}
